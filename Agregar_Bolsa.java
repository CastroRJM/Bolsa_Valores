    private void Buscar_Documentos(String bolsa){
        try{
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("Select id_doc,descripcion from documentos inner join bolsa_documentos using(id_doc)"
                    + "where id_bolsa='"+bolsa+"'");
           
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                txt_id_doc.setText(String.valueOf(rs.getInt("id_doc")));

                txt_documento.setText(rs.getString("descripcion"));
          
                txt_back1.setText(String.valueOf(rs.getInt("id_doc")));
                
                btn_actu1.setVisible(true);
                
                num_doc=1;
                
               if(rs.next()){
            txt_id_doc1.setVisible(true);
            txt_documento1.setVisible(true);
            txt_id_doc1.setText(String.valueOf(rs.getInt("id_doc")));
      
            txt_documento1.setText(rs.getString("descripcion"));
            
         
            txt_back2.setText(String.valueOf(rs.getInt("id_doc")));
                
                btn_actu2.setVisible(true);
            
            num_doc=2;
            
            
               }if(rs.next()){   
                txt_id_doc2.setVisible(true);
                txt_documento2.setVisible(true);   
                txt_id_doc2.setText(String.valueOf(rs.getInt("id_doc")));
          
                txt_documento2.setText(rs.getString("descripcion"));
                
                txt_back3.setVisible(true);
                txt_back3.setText(String.valueOf(rs.getInt("id_doc")));
                
                btn_actu3.setVisible(true);
                
                num_doc=3;
               }
               
           
            }else{
                
            }
        }catch(SQLException ex){
            
        }
        //JOptionPane.showMessageDialog(null,"Numero de documentos: "+num_doc);
    }
    
    
