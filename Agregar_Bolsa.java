    private void btn_update_bolsaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_update_bolsaActionPerformed
            if(txt_num_empleado.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Debe de ingresar el numero de bolsa","ERROR",JOptionPane.ERROR_MESSAGE);
            }else{
              try{
                  Consultas_BD eliminar=new Consultas_BD("bolsa",Integer.parseInt(txt_num_empleado.getText()));
                  Consultas_BD muerto= new Consultas_BD();
                  Consultas_BD buscar_emp = new Consultas_BD();
             
                  
                   if( this.Estatus().equals("B") && buscar_emp.Encontrar_Empleados(Integer.parseInt(txt_num_empleado.getText()))==true){
                      JOptionPane.showMessageDialog(null, "No puede poner de baja uno en bolsa,\n si ya esta registrado como Empleado","Error",JOptionPane.ERROR_MESSAGE);
                      this.Vaciar();
                      
                  }else{
                       
                        Connection cn = Conexion.conectar();
                  PreparedStatement pst = cn.prepareStatement("Update bolsa set nombre=?,apellido_p=?,apellido_m=?,sexo=?,fecha_nacimiento=?,direccion=?,telefono=?,fecha_ingreso=?,id_nac=?,id_perfil=?,id_esc=?,id_exp=?,curp=?,estatus=?,observaciones=?"
                          + " where id_empleado='"+txt_num_empleado.getText()+"'");
                  pst.setString(1, txt_nombre.getText());
                  pst.setString(2, txt_apellidoP.getText());
                  pst.setString(3, txt_apellidoM.getText());
                  pst.setString(4, this.Sexo());
                  pst.setString(5, txt_fecha_nacimiento.getText());
                  pst.setString(6, txt_direccion.getText());
                  pst.setString(7, txt_telefono.getText());
                  pst.setString(8, txt_ingreso.getText());
                  pst.setString(9, txt_id_nacion.getText());
                  pst.setString(10, txt_id_perfil.getText());
                  pst.setString(11, txt_id_escuela.getText());
                  pst.setString(12, txt_id_experiencia.getText());
                  pst.setString(13, txt_curp.getText());
                  pst.setString(14, this.Estatus());
                  pst.setString(15, txt_observaciones.getText());
                  
  
                 
                  if(this.Estatus()=="M"){
                      JOptionPane.showMessageDialog(null,txt_nombre.getText()+" ha sido eliminado");
                      muerto.Insertar_Muerto_Bolsa(txt_nombre.getText(), txt_apellidoP.getText(), txt_apellidoM.getText(),Integer.parseInt(txt_num_empleado.getText()),txt_observaciones.getText());
                      eliminar.Eliminar("id_empleado");
                  }
                      

         
               
                  pst.executeUpdate();
                  JOptionPane.showMessageDialog(null,"Actualizacion lograda");
                  this.Vaciar();
                  cn.close(); 
                   }
                  
              }catch(SQLException ex){
                  
              }
                    
            }
    }//GEN-LAST:event_btn_update_bolsaActionPerformed

