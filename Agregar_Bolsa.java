    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        if(this.estaVacio()==true){
            JOptionPane.showMessageDialog(null,"Te falta llenar campos");
            this.Vaciar();
        }else{
            try{
        id_bolsa=Integer.parseInt(txt_num_empleado.getText());
        String nombre = txt_nombre.getText().trim().toLowerCase();
        String apellidoP = txt_apellidoP.getText().trim().toLowerCase();
        String apellidoM = txt_apellidoM.getText().trim().toLowerCase();
        String sex = this.Sexo();
        String nacimiento = txt_fecha_nacimiento.getText().trim();
        String direccion = txt_direccion.getText().trim().toLowerCase();
        String telefono = txt_telefono.getText().trim();
        String ingreso = txt_ingreso.getText().trim();
        int nacion = Integer.parseInt(txt_id_nacion.getText().trim());
        int perfil = Integer.parseInt(txt_id_perfil.getText().trim());
        int escuela = Integer.parseInt(txt_id_escuela.getText().trim());
        int exp = Integer.parseInt(txt_id_experiencia.getText().trim());
        String curp = txt_curp.getText().trim().toLowerCase();
        String estatus= this.Estatus();
        String observaciones = txt_observaciones.getText().trim().toLowerCase();
        
        if(estatus.equals("B") || estatus.equals("M")){
            JOptionPane.showMessageDialog(null,"No puedes ingresar uno de bolsa con ese estatus","ERROR",JOptionPane.ERROR_MESSAGE);
        }else{
             base = new Consultas_BD();
        base.Agregar_Bolsa(id_bolsa, nombre, apellidoP, apellidoM, sex, nacimiento, direccion, telefono, ingreso, nacion, perfil, escuela, exp, curp, estatus, observaciones);
        
        
        base.Agregar_Bolsa_Documento("bolsa_documentos",id_bolsa,this.num_documentos());
            
        this.Vaciar();
        }
        
        
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null,"No debe de estar vacio ningun campo","Error",JOptionPane.ERROR);
            }
        
        }  
    }//GEN-LAST:event_btn_agregarActionPerformed
    
