private void Buscar_Cursos(String no_empleado){
     try{
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement("Select id_curso,tipo_curso from curso inner join curso_empleado using(id_curso)"
                    + "where id_empleado='"+no_empleado+"'");
           
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                txt_id_curso.setText(String.valueOf(rs.getInt("id_curso")));
                txt_curso.setText(rs.getString("tipo_curso"));
                txt_back1.setText(String.valueOf(rs.getInt("id_curso")));
                
                btn_actu1.setVisible(true);
                num_curso=1;
                
                
               if(rs.next()){
            txt_id_curso1.setVisible(true);
            txt_curso1.setVisible(true);
            txt_id_curso1.setText(String.valueOf(rs.getInt("id_curso")));
            txt_curso1.setText(rs.getString("tipo_curso"));
            txt_back2.setText(String.valueOf(rs.getInt("id_curso")));
                
                btn_actu2.setVisible(true);
            num_curso=2;
            
            
               }if(rs.next()){   
                txt_id_curso2.setVisible(true);
                txt_curso2.setVisible(true);   
                txt_id_curso2.setText(String.valueOf(rs.getInt("id_cursp")));
                txt_curso2.setText(rs.getString("tipo_curso"));
                txt_back3.setText(String.valueOf(rs.getInt("id_curso")));
                
                btn_actu3.setVisible(true);
                num_curso=3;
                
                
               }
               
           
            }else{
                
            }
        }catch(SQLException ex){
            
        }
}
    
    
