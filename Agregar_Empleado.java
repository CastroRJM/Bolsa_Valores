    private boolean EstaVacio(){
        if(txt_id_bolsa.getText().isEmpty()){
            txt_id_bolsa.setBackground(Color.red);
            Vacio = false;
        } if(txt_nombre.getText().isEmpty()){
            txt_nombre.setBackground(Color.red);
             Vacio = false;
        } if(txt_apellidoP.getText().isEmpty()){
            txt_apellidoP.setBackground(Color.red);
             Vacio = false;
        } if(txt_apellidoM.getText().isEmpty()){
            txt_apellidoM.setBackground(Color.red);
             Vacio = false;
        } if(txt_sexo.getText().isEmpty()){
            txt_sexo.setBackground(Color.red);
             Vacio = false;
        } if(txt_fecha_nacimiento.getText().isEmpty()){
            txt_fecha_nacimiento.setBackground(Color.red);
             Vacio = false;
        } if(txt_telefono.getText().isEmpty()){
            txt_telefono.setBackground(Color.red);
             Vacio = false;
        }
        
        else{
            Vacio = true;
        }
        
        return Vacio;
    }
    
    private String Estatus(){
        String estatus="";
        int pos = cmb_estatus.getSelectedIndex()+1;
        if(pos==1){
            estatus = "V";
        }if(pos==2){
            estatus = "B";
        }if(pos==3){
            estatus = "M";
        }
        return estatus;
    }
    
