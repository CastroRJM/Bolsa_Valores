    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed
        id = Integer.parseInt(txt_id_dep.getText().trim());
        departamento = txt_departamento.getText().trim().toLowerCase();
        base = new Consultas_BD("departamento",id,departamento);
        if(!departamento.equals("") && txt_id_dep.getText()!=""){

            if(base.EncontrarRepeteciones("id_depto","tipo_depto")==true){
                JOptionPane.showMessageDialog(null, "Ya hay un depto con ese id o departamento");
                txt_departamento.setText("");
            }else{
                base.Insertar();
                JOptionPane.showMessageDialog(null,"Insercion Lograda");
                this.Vaciar();
                this.CampoVerde();

            }

        }else{
            JOptionPane.showMessageDialog(null,"No debe de estar vacio");
            this.CampoRojo();
        }
    }//GEN-LAST:event_btn_ingresarActionPerformed

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        departamento= txt_departamento.getText().trim().toLowerCase();//Metodo para ingresar nacionalidad
        id = Integer.parseInt(txt_id_dep.getText());
        base = new Consultas_BD("departamento",id,departamento);
        if(!departamento.equals("") && txt_id_dep.getText()!=""){
            base.Modificar("tipo_depto","id_depto");
            this.Vaciar();
            this.CampoVerde();

        }else{
            JOptionPane.showMessageDialog(null,"No debe de estar vacio");
            this.CampoRojo();
        }
    }//GEN-LAST:event_btn_modificarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        departamento = txt_departamento.getText().trim().toLowerCase();//Metodo para ingresar nacionalidad
        id = Integer.parseInt(txt_id_dep.getText());
        base = new Consultas_BD("departamento",id,departamento);

        if(!departamento.equals("") && txt_id_dep.getText()!=""){
            base.Eliminar("id_depto");
            this.Vaciar();

        }else{
            JOptionPane.showMessageDialog(null,"No debe de estar vacio");
            this.CampoRojo();
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void txt_id_depFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_id_depFocusLost
        try{
                id = Integer.parseInt(txt_id_dep.getText());
             
             if(txt_id_dep.getText().isEmpty()){
                txt_departamento.setText("");
             }else{
                base = new Consultas_BD("departamento",id,departamento);
                txt_departamento.setText(base.Buscar("id_depto"));
             }
        }catch(NumberFormatException ex){
            
        }    
    }//GEN-LAST:event_txt_id_depFocusLost
public void Vaciar(){
    txt_id_dep.setText("");
    txt_departamento.setText("");
}
public void CampoVerde(){
    txt_id_dep.setBackground(Color.green);
    txt_departamento.setBackground(Color.green);
}
public void CampoRojo(){
    if(txt_id_dep.getText()!=""){
        txt_id_dep.setBackground(Color.red);
    }else{
        txt_departamento.setBackground(Color.red);
    }
}
